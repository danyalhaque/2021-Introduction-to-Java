package lab1;

public class CramersRule {

	public static void main(String[] args) {
		System.out.print("x = ");
		System.out.println((8.0 * 3.0 - 9.0 * 6.0) / (2.0 * 3.0 - 9.0 * 5.0));
		System.out.print("y = ");
		System.out.println((2.0 * 6.0 - 8.0 * 5.0) / (2.0 * 3.0 - 9.0 * 5.0));
	}
}