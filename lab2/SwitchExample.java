package lab2;

import java.util.Scanner;

public class SwitchExample {

	public static void main(String[] args) {

		int value = 0;

		switch (value) {
		case 0:
			System.out.println("0");
			break;
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
			break;
		default:
			System.out.println("default.");
		}
	}

}
